#include "libft.h"

int	ft_print_str(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (write(1, "(null)", 6));
	while (*str)
	{
		ft_print_char((char)*str);
		i++;
		str++;
	}
	return (i);
}
